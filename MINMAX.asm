DATA SEGMENT
    MSG1 DB,10,13,"ENTER NO.OF ELEMENTS: $"
    MSG2 DB,10,13,"ENTER ELEMENT: $"
    RES1 DB,10,13,"MINIMUM IS: $"
    RES2 DB,10,13,"MAXIMUM IS: $"
    MIN DB ?
    MAX DB ?
    COUNT DB ?
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX
    
    MOV DX,OFFSET MSG1
    MOV AH,09H
    INT 21H
    CALL INPUT
    MOV COUNT,BL
   
 
    MOV CL,COUNT
    MOV CH,00H
    MOV SI,1000H
 
    
LBLA:MOV DX,OFFSET MSG2
     MOV AH,09H
     INT 21H
     CALL INPUT
     MOV [SI],BL
     INC SI
LOOP LBLA

MOV SI,1000H
MOV CX,0000H
MOV CL,COUNT
MOV MIN,BL
MOV MAX,BL


CALMIN:MOV BX,[SI]
       CMP MIN,BL 
       JC LBL1
       MOV MIN,BL 
       LBL1:INC SI
LOOP CALMIN       
       
 
MOV SI,1000H
MOV Cx,0000H
MOV CL,COUNT

CALMAX:MOV BL,[SI]
       CMP MAX,BL
       JNC LBL2
       MOV MAX,BL
       LBL2:INC SI 
       
LOOP CALMAX

     
MOV DX,OFFSET RES1
MOV AH,09H
INT 21H
MOV BL,MIN
CALL DISP

MOV DX,OFFSET RES2
MOV AH,09H
INT 21H
MOV BL,MAX
CALL DISP

MOV AH,4CH
INT 21H

INPUT PROC
    MOV AH,01H
    INT 21H
    CALL ASCIITOHEX
    ROL AL,04H
    MOV BL,AL
    MOV AH,01H
    INT 21H
    CALL ASCIITOHEX
    ADD BL,AL
    RET
ENDP

DISP PROC
    MOV CL,BL
    AND BL,0F0H  
    ROR BL,04H
    CALL HEXTOASCII
    MOV DL,BL
    MOV AH,02H
    INT 21H
    
    MOV BL,CL
    AND BL,0FH
    CALL HEXTOASCII
    MOV DL,BL
    MOV AH,02H
    INT 21H
    RET
ENDP  


ASCIITOHEX PROC
    CMP AL,41H
    JC LBLO
    SUB AL,07H
    LBLO:SUB AL,30H
    RET
ENDP

HEXTOASCII PROC
    CMP BL,0AH
    JNZ LBLP
    ADD BL,07H
    LBLP:ADD BL,30H
    RET
ENDP
CODE ENDS
END START
        
    
        
  
        